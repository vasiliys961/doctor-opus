/**
 * Doctor Opus — AI-Навигатор (агент приложения)
 *
 * Слой-надстройка поверх существующих разделов. Сценарии описываются как данные,
 * чтобы добавление нового потока не требовало правки ядра (принцип OCP из SOLID).
 */

/** Расположение подсказки относительно подсвечиваемого элемента (совместимо с shepherd.js). */
export type GuidePlacement = 'top' | 'bottom' | 'left' | 'right'

/**
 * Действие, которое агент выполняет в интерфейсе.
 * Намеренно ограниченный безопасный набор: навигация, подсветка, текст.
 * Никаких авто-кликов по клиническим/платёжным кнопкам — только подсказки.
 */
export type AgentAction =
  | { type: 'navigate'; href: string }
  | { type: 'highlight'; selector: string; title: string; text: string; on?: GuidePlacement }
  | { type: 'message'; text: string }

/** Быстрый ответ (кнопка) внутри шага сценария. */
export interface AgentOption {
  /** Текст на кнопке. */
  label: string
  /** Действия, выполняемые при выборе варианта. */
  actions?: AgentAction[]
  /** Идентификатор следующего шага. Пусто — завершение сценария. */
  next?: string
}

/** Один шаг диалога: сообщение бота + варианты ответа. */
export interface AgentStep {
  id: string
  /** Сообщение, которое показывает агент при входе в шаг. */
  message: string
  /** Действия при входе в шаг (например, перейти в раздел и подсветить элемент). */
  onEnter?: AgentAction[]
  /** Быстрые ответы. Если пусто — шаг считается завершающим. */
  options?: AgentOption[]
}

/** Полный сценарий (поток) агента. */
export interface AgentScenario {
  id: string
  /** Короткое название для меню агента. */
  title: string
  /** Иконка/emoji для кнопки в меню. */
  icon: string
  /** Ключевые слова для распознавания запроса врача (нижний регистр). */
  keywords: string[]
  /** Идентификатор стартового шага. */
  start: string
  /** Шаги сценария, индексированные по id. */
  steps: Record<string, AgentStep>
  /** Показывать ли сценарий кнопкой в стартовом меню агента (популярные сценарии). */
  featured?: boolean
}

/** Сообщение в ленте чата агента. */
export interface AgentMessage {
  role: 'bot' | 'user'
  text: string
}
